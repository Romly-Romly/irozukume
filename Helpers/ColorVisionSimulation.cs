	// 色を指定の型・強さでの見え方へ変換する。透明度は変えずに保つ。1色覚は severity を無視してグレースケールにする。
	public static Color Simulate(ColorVisionType type, double severity, Color color)
	{
		double r = LinearLut[color.R];
		double g = LinearLut[color.G];
		double b = LinearLut[color.B];

		if (type == ColorVisionType.Monochromacy)
		{
			// 1色覚: 線形光の相対輝度を求め、同じ明るさの無彩色にする。
			byte tone = LinearToSrgb((0.2126 * r) + (0.7152 * g) + (0.0722 * b));
			return Color.FromArgb(color.A, tone, tone, tone);
		}

		double[][] table = type switch
		{
			ColorVisionType.Protan => ProtanMatrices,
			ColorVisionType.Tritan => TritanMatrices,
			_ => DeutanMatrices,
		};

		double[] m = InterpolateMatrix(table, severity);
		return ApplyMatrix(m, color.A, r, g, b);
	}




	// 表の中から指定の severity (0–1) に当たる行列を作る。severity を0.1刻みの位置に直し、下と上の2枚を端数で線形補間する。区切りにぴったり当たる、または下端・上端のときはその1枚をそのまま返す。
	private static double[] InterpolateMatrix(double[][] table, double severity)
	{
		double position = Math.Clamp(severity, 0.0, 1.0) * (table.Length - 1);
		int lo = (int)Math.Floor(position);
		int hi = Math.Min(lo + 1, table.Length - 1);
		double t = position - lo;

		if (lo == hi || t <= 0.0)
		{
			return table[lo];
		}

		double[] low = table[lo];
		double[] high = table[hi];
		var result = new double[9];

		for (int i = 0; i < 9; i++)
		{
			result[i] = (low[i] * (1.0 - t)) + (high[i] * t);
		}

		return result;
	}




	// 線形 RGB に変換行列を掛け、sRGB へ戻した色を組み立てる。行列の出力は 0–1 の範囲を外れることがあるため、符号化の段でクランプする。
	private static Color ApplyMatrix(double[] m, byte alpha, double r, double g, double b)
	{
		byte sr = LinearToSrgb((m[0] * r) + (m[1] * g) + (m[2] * b));
		byte sg = LinearToSrgb((m[3] * r) + (m[4] * g) + (m[5] * b));
		byte sb = LinearToSrgb((m[6] * r) + (m[7] * g) + (m[8] * b));
		return Color.FromArgb(alpha, sr, sg, sb);
	}




	// 線形光 (0–1) を sRGB の1チャンネル (0–255) へ符号化する。範囲外の値は端へクランプする。
	private static byte LinearToSrgb(double value)
	{
		double v = Math.Clamp(value, 0.0, 1.0);
		double encoded = v <= 0.0031308 ? v * 12.92 : (1.055 * Math.Pow(v, 1.0 / 2.4)) - 0.055;
		return (byte)Math.Round(encoded * 255.0);
	}




	// sRGB の1チャンネル (0–255) を線形光 (0–1) へ戻す。
	private static double SrgbToLinear(byte channel)
	{
		double v = channel / 255.0;
		return v <= 0.04045 ? v / 12.92 : Math.Pow((v + 0.055) / 1.055, 2.4);
	}




	// sRGB の256段を線形光へ戻す前計算表を作る。
	private static double[] BuildLinearLut()
	{
		var lut = new double[256];

		for (int i = 0; i < 256; i++)
		{
			lut[i] = SrgbToLinear((byte)i);
		}

		return lut;
	}
