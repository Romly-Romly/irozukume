namespace Irozukume.Models;

// 作れる色を表示上どの制約へ丸めるか。色1・色2の RGB は常にフルカラーで保持し、これは表示・グラデーション・パッドの丸めだけを切り替える表示レンズとして使う。None は丸めず素の色を見せる。WebSafe は各チャンネルを 6 段へ、RGB555・RGB444・RGB332 は各チャンネルを指定ビット数(5-5-5・4-4-4・3-3-2)へ量子化する格子モード。Term256・Term16・Term8 はターミナルのインデックスパレットへ最も近い色を選ぶ。格子モードと違いチャンネル独立では決まらないため、距離計算による最近傍探索で丸める。
public enum ColorLimitMode
{
	// 丸めず、作った色をそのまま見せる。
	None,

	// 各チャンネルを最も近い Web セーフカラー(0x00・0x33・0x66・0x99・0xCC・0xFF の 6 段)へ丸める。216 色。
	WebSafe,

	// R・B を 5 ビット(32 段)、G を 6 ビット(64 段)へ量子化する。65536 色。
	Rgb565,

	// 各チャンネルを 5 ビット(32 段)へ量子化する。32768 色。
	Rgb555,

	// 各チャンネルを 4 ビット(16 段)へ量子化する。4096 色。
	Rgb444,

	// R・G を 3 ビット(8 段)、B を 2 ビット(4 段)へ量子化する。256 色。
	Rgb332,

	// ターミナルの 256 色パレット(基本16色 + 6×6×6 キューブ + 24段グレー)の中から最も近い色を選ぶ。
	Term256,

	// ターミナルの基本16色の中から最も近い色を選ぶ。実RGBは参照テーマで決まる。
	Term16,

	// ターミナルの基本8色(0-7)の中から最も近い色を選ぶ。実RGBは参照テーマで決まる。
	Term8,
}
